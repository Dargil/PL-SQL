--PRACTICA2- FRASE AL REVES
DECLARE
    FRASE VARCHAR2(100);
    LIMITE NUMBER;
    CONTADOR NUMBER;
    FRASE_AL_REVES VARCHAR2(100);
BEGIN
    FRASE:='ESTO ES UNA PRUEBA DE FRSE';
    LIMITE:=LENGTH(FRASE);
    WHILE LIMITE>0 LOOP
        FRASE_AL_REVES:=FRASE_AL_REVES||SUBSTR(FRASE,LIMITE,1);
        LIMITE:=LIMITE-1;
    END LOOP;
        DBMS_OUTPUT.PUT_LINE(FRASE_AL_REVES);
END;
/

