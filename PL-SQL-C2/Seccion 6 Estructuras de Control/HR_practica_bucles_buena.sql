--PRACTICA 5- MULTIPLOS DE 4
DECLARE
    INICIO NUMBER;
    FINAL NUMBER;
BEGIN
  INICIO:=10;
  FINAL:=200;
  FOR I IN INICIO..FINAL LOOP
    IF MOD(I,4)=0 THEN
        DBMS_OUTPUT.PUT_LINE(I);
    END IF;
   END LOOP;
END;
